package main

import "testing"

func TestPerimeter(t *testing.T) {
	got := Perimeter(10.0, 10.0)
	want := 40.0

	if got != want {
		// f : float64用
		// .2 : 小数点以下2桁を出力
		t.Errorf("got %.2f want %.2f", got, want)
	}
}

